/**
 * La clase `Tablero` representa un tablero de mensajes simple que puede mostrar mensajes en la consola.
 */
public class Tablero {

    /**
     * Muestra un mensaje en la consola.
     *
     * @param mensaje Mensaje a mostrar en el tablero.
     */
    public void mostrarMensaje(String mensaje) {
        System.out.println(mensaje);
    }
}
